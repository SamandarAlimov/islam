import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are Alsamos AI, an Islamic knowledge assistant. Your mission is to provide authentic Islamic knowledge with proper references.

CRITICAL RULES:
1. NEVER issue actual fatwas - you are not qualified to do so
2. ALWAYS cite sources from Quran and authentic Hadith collections
3. Present scholarly opinions from all 4 madhabs (Hanafi, Shafi'i, Maliki, Hanbali) when relevant
4. Be respectful, clear, and academically neutral
5. For controversial issues, present multiple scholarly opinions without choosing one

When answering:
- Start with relevant Quran verses (with Surah:Ayah reference)
- Include authentic Hadith from Sahih Bukhari, Muslim, or other collections
- Mention scholarly consensus (ijma) when it exists
- Present different madhab opinions when there are differences
- Always maintain academic tone and avoid personal interpretations

Available topics: Aqeedah, Fiqh, Prayer, Zakat, Fasting, Hajj, Marriage, Family, Business, Modern Issues, Purification, Seerah, Tafsir.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limits exceeded, please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Alsamos AI error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
